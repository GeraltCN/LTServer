CREATE TABLE USER(

  USERNAME TEXT PRIMARY KEY NOT NULL ,
  HEAD BLOB ,

  HISTORY_ORDER BLOB ,
  PRESENT_ORDER BLOB ,

  DC BLOB ,
  AP INTEGER NOT NULL ,

  DRIVERS BLOB
);



CREATE TABLE DRIVER(

  DRIVERNAME TEXT PRIMARY KEY NOT NULL ,

  HISTORY_ORDER BLOB,
  PRESENT_ORDER BLOB
);



CREATE TABLE "ORDER"(

  ID TEXT PRIMARY KEY NOT NULL ,
  USERNAME TEXT NOT NULL ,
  DRIVERNAME TEXT NOT NULL ,
  START_TIME REAL  NOT NULL ,
  END_TIME REAL ,
  REMARK TEXT
);

